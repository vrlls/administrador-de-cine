    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (!id.getText().equals("")) {
            try {
                Nodo ptr = m.agregarnodosReserva(arch3);
                Nodo q = m.consultarReserva(ptr, id.getText());
                if (q != null) {
                    nom.setText(q.id + " \n " + q.horaI + " \n " + q.HoraF + " \n " + q.cantidad + " \n " + q.pelicula);
                    File pelicula = new File(q.Sala + q.pelicula + q.horaI.substring(0, q.horaI.length() - 3) + ".txt");
                   // System.out.println("file " + pelicula.getName());
                    if (pelicula.exists()) {
                        m.EliminarReserva(arch3, q, ptr);
                        TableCR t = new TableCR(pelicula);
                        t.setVisible(true);
                    } else{
                        JOptionPane.showMessageDialog(null, "esta pelicula fue retirada de cartelera");
                        m.EliminarReserva(arch3, q, ptr);
                    }
                } else {
                    nom.setText("Esta persona no ha hecho reservas");
                }
            } catch (IOException ex) {
                Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Digite una id");
        }


    }//GEN-LAST:event_jButton4ActionPerformed

