    private void consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultarActionPerformed
        if (!id.getText().equals("")) {
            Nodo ptr = null;
            try {
                ptr = m.agregarnodosReserva(arch3);
            } catch (IOException ex) {
                Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
            }
            Nodo q = m.consultarReserva(ptr, id.getText());
            if (q != null) {
                nom.setText(q.pelicula);
                hora.setText(q.horaI+" - "+q.HoraF);
                cant.setText(q.cantidad);
                ID.setText(q.id);
            } else {
                nom.setText("Esta persona no ha hecho reservas");
                hora.setText("");
                cant.setText("");
                ID.setText("");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Digite un documento");
        }
    }//GEN-LAST:event_consultarActionPerformed

