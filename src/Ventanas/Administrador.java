    private void ContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ContinuarActionPerformed
        if ((Sala1.isSelected() || Sala2.isSelected()) && !pelicula.getSelectedItem().equals("Escoja pelicula") && !horario.getSelectedItem().equals("Escoja horario") && !cantidad.getText().equals("")) {
            Nodo ptr = null;
            try {
                if (Sala1.isSelected()) {
                    ptr = m.agregarnodos(arch);
                    Nodo q = m.comprar(ptr, horario.getSelectedItem().toString(), pelicula.getSelectedItem().toString(), cantidad.getText());
                    if (q != null) {
                        m.guardar(arch, q);
                        Table t = new Table(m.creararchivo("S1" + pelicula.getSelectedItem().toString(), horario.getSelectedItem().toString()));
                        t.setVisible(true);
                        this.setVisible(false);
                    } else {
                        JOptionPane.showMessageDialog(null, "La cantidad de boletas que necesita, no esta disponible");
                    }
                } else {
                    if (Sala2.isSelected()) {
                        ptr = m.agregarnodos(arch2);
                        Nodo q = m.comprar(ptr, horario.getSelectedItem().toString(), pelicula.getSelectedItem().toString(), cantidad.getText());
                        if (q != null) {
                            m.guardar(arch2, q);
                            Table t = new Table(m.creararchivo("S2" + pelicula.getSelectedItem().toString(), horario.getSelectedItem().toString()));
                            t.setVisible(true);
                            this.setVisible(false);
                        } else {
                            JOptionPane.showMessageDialog(null, "La cantidad de boletas que necesita, no esta disponible");
                        }
                    }
                }
            } catch (IOException ex) {
                Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione todos los campos");
        }

    }//GEN-LAST:event_ContinuarActionPerformed

